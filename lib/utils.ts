/**
 * Smooth scroll to element by ID
 */
export function scrollToElement(elementId: string, offset: number = 0): void {
  const element = document.getElementById(elementId);
  if (!element) return;

  const elementPosition = element.getBoundingClientRect().top + window.scrollY;
  const offsetPosition = elementPosition - offset;

  window.scrollTo({
    top: offsetPosition,
    behavior: 'smooth',
  });
}

/**
 * Smooth scroll to section with navbar offset
 */
export function scrollToSection(sectionId: string): void {
  const NAVBAR_HEIGHT = 80; // Adjust based on your navbar height
  scrollToElement(sectionId, NAVBAR_HEIGHT);
}

/**
 * Check if element is in viewport
 */
export function isInViewport(element: HTMLElement, threshold: number = 0.5): boolean {
  const rect = element.getBoundingClientRect();
  const windowHeight = window.innerHeight || document.documentElement.clientHeight;
  const windowWidth = window.innerWidth || document.documentElement.clientWidth;

  const verticalVisible = rect.top <= windowHeight * (1 - threshold) && rect.bottom >= windowHeight * threshold;
  const horizontalVisible = rect.left <= windowWidth && rect.right >= 0;

  return verticalVisible && horizontalVisible;
}


/**
 * Format number with thousand separators
 * @example formatNumber(1234567) => "1,234,567"
 */
export function formatNumber(value: number): string {
  return new Intl.NumberFormat('en-US').format(value);
}

/**
 * Format number as currency (USD)
 * @example formatCurrency(1234567) => "$1,234,567"
 */
export function formatCurrency(value: number, decimals: number = 0): string {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    minimumFractionDigits: decimals,
    maximumFractionDigits: decimals,
  }).format(value);
}

/**
 * Format large numbers with suffixes (K, M, B)
 * @example formatCompact(1234567) => "1.2M"
 */
export function formatCompact(value: number, decimals: number = 1): string {
  const formatter = new Intl.NumberFormat('en-US', {
    notation: 'compact',
    compactDisplay: 'short',
    minimumFractionDigits: 0,
    maximumFractionDigits: decimals,
  });
  return formatter.format(value);
}

/**
 * Format percentage
 * @example formatPercent(0.573) => "57.3%"
 */
export function formatPercent(value: number, decimals: number = 1): string {
  return `${(value * 100).toFixed(decimals)}%`;
}

/**
 * Format square footage
 * @example formatSquareFeet(45000) => "45,000 sq ft"
 */
export function formatSquareFeet(value: number): string {
  return `${formatNumber(value)} sq ft`;
}


/**
 * Check if device is mobile
 */
export function isMobile(): boolean {
  if (typeof window === 'undefined') return false;
  return window.innerWidth < 768;
}

/**
 * Check if device is tablet
 */
export function isTablet(): boolean {
  if (typeof window === 'undefined') return false;
  return window.innerWidth >= 768 && window.innerWidth < 1024;
}

/**
 * Check if device is desktop
 */
export function isDesktop(): boolean {
  if (typeof window === 'undefined') return false;
  return window.innerWidth >= 1024;
}

/**
 * Check if browser supports specific features
 */
export function supportsFeature(feature: 'intersection-observer' | 'webp' | 'avif'): boolean {
  if (typeof window === 'undefined') return false;

  switch (feature) {
    case 'intersection-observer':
      return 'IntersectionObserver' in window;
    case 'webp':
      return document.createElement('canvas').toDataURL('image/webp').indexOf('data:image/webp') === 0;
    case 'avif':
      return document.createElement('canvas').toDataURL('image/avif').indexOf('data:image/avif') === 0;
    default:
      return false;
  }
}

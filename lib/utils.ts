/**
 * Check if video should autoplay based on viewport visibility
 */
export function shouldAutoplayVideo(video: HTMLVideoElement, threshold: number = 0.5): boolean {
  return isInViewport(video, threshold);
}

/**
 * Safely play video with error handling
 */
export async function safePlayVideo(video: HTMLVideoElement): Promise<void> {
  try {
    await video.play();
  } catch (error) {
    // Autoplay was prevented - this is expected in some browsers
    console.debug('Video autoplay prevented:', error);
  }
}

/**
 * Safely pause video
 */
export function safePauseVideo(video: HTMLVideoElement): void {
  if (!video.paused) {
    video.pause();
  }
}

